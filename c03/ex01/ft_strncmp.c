int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n == 0)
		return (0);
	while (1)
	{
		if (*s1 != *s2++)
			return (*s1 - *--s2);
		if (*s1++ == 0)
			break ;
		if (--n == 0)
			break ;
	}
	return (0);
}
